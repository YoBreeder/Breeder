export function useT() {
  const lang = typeof window !== 'undefined'
    ? localStorage.getItem('yob_lang') || 'en'
    : 'en'
  return t[lang] || t.en
}

// Words in all 5 languages interleaved for the ticker
const WORDS_EN = ['intercourse', 'penetration', 'lovemaking', 'copulation', 'fornication', 'mating', 'procreate']
const WORDS_ES = ['relaciones', 'penetración', 'hacer el amor', 'cópula', 'fornicación', 'apareamiento', 'procrear']
const WORDS_FR = ['rapports', 'pénétration', 'faire l\'amour', 'copulation', 'fornication', 'accouplement', 'procréer']
const WORDS_IT = ['rapporti', 'penetrazione', 'fare l\'amore', 'copulazione', 'fornicazione', 'accoppiamento', 'procreare']
const WORDS_HI = ['संभोग', 'प्रवेश', 'प्रेम क्रीड़ा', 'समागम', 'व्यभिचार', 'मिलन', 'प्रजनन']

export function buildTicker(lang) {
  const en = WORDS_EN
  let other
  if (lang === 'es') other = WORDS_ES
  else if (lang === 'fr') other = WORDS_FR
  else if (lang === 'it') other = WORDS_IT
  else if (lang === 'hi') other = WORDS_HI
  else other = WORDS_ES // default alternate is Spanish

  // Interleave: EN · OTHER · EN · OTHER …
  const mixed = []
  for (let i = 0; i < en.length; i++) {
    mixed.push(en[i])
    mixed.push(other[i])
  }
  const str = mixed.join('  ·  ')
  return str + '  ·  ' + str + '  ·  '
}
